func TestRunFromBytes_Idempotent(t *testing.T) {
	tmp := t.TempDir()
	yaml := loadYAML(t)

	if err := RunFromBytes(yaml, tmp, false); err != nil {
		t.Fatalf("first run: %v", err)
	}
	first := readAllGenerated(t, tmp)

	if err := RunFromBytes(yaml, tmp, false); err != nil {
		t.Fatalf("second run: %v", err)
	}
	second := readAllGenerated(t, tmp)

	if len(first) != len(second) {
		t.Fatalf("file set drift: first=%d second=%d", len(first), len(second))
	}
	for path, want := range first {
		got, ok := second[path]
		if !ok {
			t.Errorf("missing %s on second run", path)
			continue
		}
		if !bytes.Equal(got, want) {
			t.Errorf("idempotency broken for %s", path)
		}
	}
}

func TestRunFromBytes_WriteIfChangedSkipsUnchanged(t *testing.T) {
	tmp := t.TempDir()
	yaml := loadYAML(t)

	if err := RunFromBytes(yaml, tmp, false); err != nil {
		t.Fatalf("first run: %v", err)
	}
	target := filepath.Join(tmp, "backend/internal/shared/types/enums.go")
	info1, err := os.Stat(target)
	if err != nil {
		t.Fatalf("stat 1: %v", err)
	}
	mtime1 := info1.ModTime()

	// Sleep barrier removed: second run should skip write entirely when content matches,
	// so mtime stays equal regardless of clock granularity.
	if err := RunFromBytes(yaml, tmp, false); err != nil {
		t.Fatalf("second run: %v", err)
	}
	info2, err := os.Stat(target)
	if err != nil {
		t.Fatalf("stat 2: %v", err)
	}
	if !info2.ModTime().Equal(mtime1) {
		t.Errorf("expected mtime to be preserved on idempotent re-run; got %v vs %v", info2.ModTime(), mtime1)
	}
}

