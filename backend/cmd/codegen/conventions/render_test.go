// repoRootForTest walks up from the test working dir to the easyinterview repo root.
// The test binary runs in backend/cmd/codegen/conventions, so repo root is 4 dirs up.
func repoRootForTest(t *testing.T) string {
	t.Helper()
	wd, err := os.Getwd()
	if err != nil {
		t.Fatalf("Getwd: %v", err)
	}
	return filepath.Clean(filepath.Join(wd, "..", "..", "..", ".."))
}

func loadYAML(t *testing.T) []byte {
	t.Helper()
	data, err := os.ReadFile(filepath.Join(repoRootForTest(t), "shared", "conventions.yaml"))
	if err != nil {
		t.Fatalf("read conventions.yaml: %v", err)
	}
	return data
}

