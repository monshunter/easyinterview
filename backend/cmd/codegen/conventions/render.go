// Spec mirrors shared/conventions.yaml; the generator unmarshals this struct via yaml.v3.
type Spec struct {
	Version       float64 `yaml:"version"`
	SchemaVersion int     `yaml:"schemaVersion"`
	SampleUUIDV7  string  `yaml:"sampleUuidV7"`
	UUIDV7Regex   string  `yaml:"uuidV7Regex"`
	TmpIDPrefix   string  `yaml:"tmpIdPrefix"`
	Pagination    struct {
		DefaultPageSize int `yaml:"defaultPageSize"`
		MaxPageSize     int `yaml:"maxPageSize"`
	} `yaml:"pagination"`
	Idempotency struct {
		TTLSeconds int `yaml:"ttlSeconds"`
	} `yaml:"idempotency"`
	Errors       []ErrorSpec       `yaml:"errors"`
	JobStatuses  []string          `yaml:"jobStatuses"`
	Enums        []EnumSpec        `yaml:"enums"`
	Structures   map[string]Struct `yaml:"structures"`
	AIVocabulary struct {
		Capabilities           []string      `yaml:"capabilities"`
		ProviderRegistryFields []AIFieldSpec `yaml:"providerRegistryFields"`
		ModelProfileFields     []AIFieldSpec `yaml:"modelProfileFields"`
		Fields                 []AIFieldSpec `yaml:"fields"`
	} `yaml:"aiVocabulary"`
}

type ErrorSpec struct {
	Code      string `yaml:"code"`
	Message   string `yaml:"message"`
	Retryable bool   `yaml:"retryable"`
}

type EnumSpec struct {
	Name          string   `yaml:"name"`
	SourceSection string   `yaml:"sourceSection"`
	JSONField     string   `yaml:"jsonField"`
	Values        []string `yaml:"values"`
}

type Struct struct {
	Fields []Field `yaml:"fields"`
}

type Field struct {
	Name     string `yaml:"name"`
	Type     string `yaml:"type"`
	Optional bool   `yaml:"optional"`
	Nullable bool   `yaml:"nullable"`
}

type AIFieldSpec struct {
	Name string `yaml:"name"`
}

// Run loads the YAML at yamlPath and renders all generated files under repoRoot.
func Run(yamlPath, repoRoot string, verbose bool) error {
	data, err := os.ReadFile(yamlPath)
	if err != nil {
		return fmt.Errorf("read yaml: %w", err)
	}
	return RunFromBytes(data, repoRoot, verbose)
}

// RunFromBytes is the testable entry point that takes already-loaded YAML bytes.
func RunFromBytes(data []byte, repoRoot string, verbose bool) error {
	var spec Spec
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return fmt.Errorf("parse yaml: %w", err)
	}

	targets := []struct {
		path   string
		render func(*Spec) ([]byte, error)
	}{
		{filepath.Join(repoRoot, "backend/internal/shared/types/enums.go"), renderGoEnums},
		{filepath.Join(repoRoot, "backend/internal/shared/types/http_dto.go"), renderGoHTTPDTO},
		{filepath.Join(repoRoot, "backend/internal/shared/errors/codes.go"), renderGoErrorCodes},
		{filepath.Join(repoRoot, "backend/internal/shared/idx/generated.go"), renderGoIdx},
		{filepath.Join(repoRoot, "backend/internal/shared/ai/vocabulary.go"), renderGoAIVocabulary},
		{filepath.Join(repoRoot, "frontend/src/lib/conventions/enums.ts"), renderTSEnums},
		{filepath.Join(repoRoot, "frontend/src/lib/conventions/errors.ts"), renderTSErrors},
		{filepath.Join(repoRoot, "frontend/src/lib/conventions/ai.ts"), renderTSAIVocabulary},
		{filepath.Join(repoRoot, "frontend/src/lib/conventions/pagination.ts"), renderTSPagination},
		{filepath.Join(repoRoot, "frontend/src/lib/ids/generated.ts"), renderTSIds},
	}

	for _, t := range targets {
		content, err := t.render(&spec)
		if err != nil {
			return fmt.Errorf("render %s: %w", t.path, err)
		}
		if err := writeIfChanged(t.path, content); err != nil {
			return fmt.Errorf("write %s: %w", t.path, err)
		}
		if verbose {
			fmt.Fprintf(os.Stderr, "rendered %s (%d bytes)\n", t.path, len(content))
		}
	}
	return nil
}

func writeIfChanged(path string, content []byte) error {
	if existing, err := os.ReadFile(path); err == nil && bytes.Equal(existing, content) {
		return nil
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, content, 0o644)
}

// pascalSuffix turns a lower_snake_case enum value into a PascalCase suffix
// suitable for Go const naming, e.g. "retry_current_round" -> "RetryCurrentRound".
func pascalSuffix(value string) string {
	parts := strings.Split(value, "_")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "")
}

// upperSnakeFromPascal turns a PascalCase identifier into UPPER_SNAKE_CASE,
// e.g. "PracticeMode" → "PRACTICE_MODE".
func upperSnakeFromPascal(name string) string {
	var b strings.Builder
	for i, r := range name {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		if r >= 'a' && r <= 'z' {
			b.WriteRune(r - 32)
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

// goPlural returns the English plural of a Go identifier, handling the common
// "ends in s/x/z" case so that `Status` → `Statuses`, not `Statuss`.
func goPlural(name string) string {
	if strings.HasSuffix(name, "s") || strings.HasSuffix(name, "x") || strings.HasSuffix(name, "z") {
		return name + "es"
	}
	return name + "s"
}

// errorPascal turns an UPPER_SNAKE_CASE error code into a PascalCase Go identifier
// suitable for the "Code" prefix, e.g. "AUTH_UNAUTHORIZED" → "AuthUnauthorized".
func errorPascal(code string) string {
	parts := strings.Split(code, "_")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + strings.ToLower(p[1:])
	}
	return strings.Join(parts, "")
}

func fieldPascal(name string) string {
	return pascalSuffix(name)
}

// allEnums returns the §5 enums plus a synthetic JobStatus enum from §4.2.
func allEnums(s *Spec) []EnumSpec {
	out := make([]EnumSpec, 0, len(s.Enums)+1)
	out = append(out, s.Enums...)
	out = append(out, EnumSpec{
		Name:          "JobStatus",
		SourceSection: "4.2",
		JSONField:     "status",
		Values:        s.JobStatuses,
	})
	return out
}

