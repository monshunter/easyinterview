func renderGoEnums(s *Spec) ([]byte, error) {
	type constEntry struct {
		ConstName string
		Value     string
	}
	type enumView struct {
		Name      string
		Section   string
		JSONField string
		Consts    []constEntry
	}

	enums := allEnums(s)
	views := make([]enumView, 0, len(enums))
	for _, e := range enums {
		view := enumView{
			Name:      e.Name,
			Section:   e.SourceSection,
			JSONField: e.JSONField,
		}
		for _, v := range e.Values {
			view.Consts = append(view.Consts, constEntry{
				ConstName: e.Name + pascalSuffix(v),
				Value:     v,
			})
		}
		views = append(views, view)
	}

	var buf bytes.Buffer
	buf.WriteString(generatedHeader)
	buf.WriteString("package types\n\n")

	for _, v := range views {
		fmt.Fprintf(&buf, "// %s mirrors shared/conventions.yaml sourceSection %s (jsonField: %q).\n", v.Name, v.Section, v.JSONField)
		fmt.Fprintf(&buf, "type %s string\n\n", v.Name)
		fmt.Fprintf(&buf, "const (\n")
		for _, c := range v.Consts {
			fmt.Fprintf(&buf, "\t%s %s = %q\n", c.ConstName, v.Name, c.Value)
		}
		fmt.Fprintf(&buf, ")\n\n")
		fmt.Fprintf(&buf, "// All%s lists every defined value in declaration order.\n", goPlural(v.Name))
		fmt.Fprintf(&buf, "var All%s = []%s{\n", goPlural(v.Name), v.Name)
		for _, c := range v.Consts {
			fmt.Fprintf(&buf, "\t%s,\n", c.ConstName)
		}
		fmt.Fprintf(&buf, "}\n\n")
	}

	return formatGo(buf.Bytes())
}

func renderGoHTTPDTO(s *Spec) ([]byte, error) {
	page, ok := s.Structures["PageInfo"]
	if !ok {
		return nil, fmt.Errorf("structures.PageInfo missing")
	}

	var buf bytes.Buffer
	buf.WriteString(generatedHeader)
	buf.WriteString("package types\n\n")
	buf.WriteString("// Cursor pagination defaults from shared/conventions.yaml.\n")
	fmt.Fprintf(&buf, "const (\n\tDefaultPageSize = %d\n\tMaxPageSize     = %d\n)\n\n", s.Pagination.DefaultPageSize, s.Pagination.MaxPageSize)
	buf.WriteString("// IdempotencyKeyTTLSeconds is the B1-documented 24h TTL.\n")
	fmt.Fprintf(&buf, "const IdempotencyKeyTTLSeconds = %d\n\n", s.Idempotency.TTLSeconds)

	buf.WriteString("// PageInfo is the B1 cursor pagination wire shape.\n")
	buf.WriteString("type PageInfo struct {\n")
	for _, f := range page.Fields {
		goName := strings.ToUpper(f.Name[:1]) + f.Name[1:]
		goType := goTypeFor(f)
		fmt.Fprintf(&buf, "\t%s %s `json:%q`\n", goName, goType, f.Name)
	}
	buf.WriteString("}\n")

	return formatGo(buf.Bytes())
}

func goTypeFor(f Field) string {
	var base string
	switch f.Type {
	case "string":
		base = "string"
	case "int":
		base = "int"
	case "bool":
		base = "bool"
	case "object":
		base = "map[string]any"
	default:
		base = f.Type
	}
	if f.Nullable {
		return "*" + base
	}
	return base
}

