func renderTSEnums(s *Spec) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString(generatedTSHeader)

	enums := allEnums(s)
	for i, e := range enums {
		if i > 0 {
			buf.WriteString("\n")
		}
		fmt.Fprintf(&buf, "// %s mirrors shared/conventions.yaml sourceSection %s (jsonField: %q).\n", e.Name, e.SourceSection, e.JSONField)
		fmt.Fprintf(&buf, "export type %s =\n", e.Name)
		for j, v := range e.Values {
			sep := "|"
			fmt.Fprintf(&buf, "  %s '%s'", sep, v)
			if j == len(e.Values)-1 {
				buf.WriteString(";\n")
			} else {
				buf.WriteString("\n")
			}
		}
		buf.WriteString("\n")
		fmt.Fprintf(&buf, "export const ALL_%s: readonly %s[] = [\n", upperSnakeFromPascal(goPlural(e.Name)), e.Name)
		for _, v := range e.Values {
			fmt.Fprintf(&buf, "  '%s',\n", v)
		}
		buf.WriteString("] as const;\n")
	}

	return buf.Bytes(), nil
}

