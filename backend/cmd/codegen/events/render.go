var camelBoundary = regexp.MustCompile(`([a-z0-9])([A-Z])`)

func camelToSnake(value string) string {
	return strings.ToLower(camelBoundary.ReplaceAllString(value, `${1}_${2}`))
}

func pascal(value string) string {
	parts := regexp.MustCompile(`[._-]`).Split(value, -1)
	for i, part := range parts {
		if part == "" {
			continue
		}
		parts[i] = strings.ToUpper(part[:1]) + part[1:]
	}
	return strings.Join(parts, "")
}

func upperSnake(value string) string {
	replaced := strings.NewReplacer(".", "_", "-", "_").Replace(value)
	return strings.ToUpper(camelToSnake(replaced))
}

func tsUnion(values []string) string {
	parts := make([]string, 0, len(values))
	for _, value := range values {
		parts = append(parts, fmt.Sprintf("%q", value))
	}
	return strings.Join(parts, " | ")
}

func quotedList(values []string) string {
	parts := make([]string, 0, len(values))
	for _, value := range values {
		parts = append(parts, fmt.Sprintf("%q", value))
	}
	return strings.Join(parts, ", ")
}

func writeStringSlice(buf *bytes.Buffer, name string, values []string) {
	fmt.Fprintf(buf, "var %s = []string{\n", name)
	for _, value := range values {
		fmt.Fprintf(buf, "\t%q,\n", value)
	}
	buf.WriteString("}\n\n")
}

