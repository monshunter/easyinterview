func jsonSchemaForType(typ string) map[string]any {
	switch typ {
	case "int":
		return map[string]any{"type": "integer"}
	case "bool":
		return map[string]any{"type": "boolean"}
	case "string[]":
		return map[string]any{"type": "array", "items": map[string]any{"type": "string"}}
	default:
		if strings.HasPrefix(typ, "$ref:b1.") {
			return map[string]any{"$ref": "../refs/" + strings.TrimPrefix(typ, "$ref:b1.") + ".json"}
		}
		if strings.HasPrefix(typ, "$ref:event.") {
			return map[string]any{"$ref": "../refs/" + strings.TrimPrefix(typ, "$ref:event.") + ".json"}
		}
		return map[string]any{"type": "string"}
	}
}

func collectEnumRefs(e *EventsSpec) map[string][]string {
	refs := map[string][]string{}
	for _, enum := range e.EventLocalEnums {
		refs[enum.Name] = enum.Values
	}
	for _, event := range e.Events {
		for _, field := range event.RequiredPayload {
			collectEnumRef(field.Type, refs)
		}
		for _, field := range event.OptionalPayload {
			collectEnumRef(field.Type, refs)
		}
	}
	return refs
}

func collectEnumRef(typ string, refs map[string][]string) {
	if strings.HasPrefix(typ, "$ref:b1.") {
		name := strings.TrimPrefix(typ, "$ref:b1.")
		if values, ok := b1EnumValues[name]; ok {
			refs[name] = values
		}
	}
}

