func baselinePayload(fields map[string]PayloadField) map[string]map[string]string {
	out := make(map[string]map[string]string, len(fields))
	for name, field := range fields {
		out[name] = map[string]string{
			"type":   field.Type,
			"source": field.Source,
		}
		if field.Source == "" {
			delete(out[name], "source")
		}
	}
	return out
}

func renderEnumRef(name string, values []string) ([]byte, error) {
	return marshalJSON(map[string]any{
		"$schema": "https://json-schema.org/draft/2020-12/schema",
		"$id":     "shared/events/refs/" + name + ".json",
		"title":   name,
		"type":    "string",
		"enum":    values,
	})
}

func marshalJSON(v any) ([]byte, error) {
	var buf bytes.Buffer
	enc := json.NewEncoder(&buf)
	enc.SetEscapeHTML(false)
	enc.SetIndent("", "  ")
	if err := enc.Encode(v); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

func formatGo(src []byte) ([]byte, error) {
	formatted, err := format.Source(src)
	if err != nil {
		return nil, fmt.Errorf("gofmt: %w\n%s", err, src)
	}
	return formatted, nil
}

func sortedPayloadFields(fields map[string]PayloadField) []string {
	out := make([]string, 0, len(fields))
	for name := range fields {
		out = append(out, name)
	}
	sort.Strings(out)
	return out
}

func eventNames(e *EventsSpec) []string {
	out := make([]string, 0, len(e.Events))
	for _, event := range e.Events {
		out = append(out, event.Name)
	}
	return out
}

func producerValues(e *EventsSpec) []string {
	seen := map[string]bool{}
	for _, field := range e.Envelope.Fields {
		if field.Name == "producer" {
			for _, value := range field.Values {
				seen[value] = true
			}
		}
	}
	out := make([]string, 0, len(seen))
	for value := range seen {
		out = append(out, value)
	}
	sort.Strings(out)
	return out
}

func producerList(value any) []string {
	switch v := value.(type) {
	case string:
		return []string{v}
	case []any:
		out := make([]string, 0, len(v))
		for _, item := range v {
			out = append(out, fmt.Sprint(item))
		}
		return out
	case []string:
		return v
	default:
		return nil
	}
}

func goType(typ string, optional bool) string {
	base := "string"
	switch {
	case typ == "uuidv7", typ == "string", typ == "slug", typ == "bcp47", typ == "opaque_ref":
		base = "string"
	case typ == "string[]":
		base = "[]string"
	case typ == "int":
		base = "int"
	case typ == "bool":
		base = "bool"
	case strings.HasPrefix(typ, "$ref:b1."):
		base = "sharedtypes." + strings.TrimPrefix(typ, "$ref:b1.")
	case strings.HasPrefix(typ, "$ref:event."):
		base = strings.TrimPrefix(typ, "$ref:event.")
	}
	if optional && !strings.HasPrefix(base, "[]") {
		return "*" + base
	}
	return base
}

